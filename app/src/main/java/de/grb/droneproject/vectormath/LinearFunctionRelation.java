package de.grb.droneproject.vectormath;

/**
 * This is an enum with the possible relations of two Linear Functions
 * @author L. Janke
 */
public enum LinearFunctionRelation {
    /**
     * The two functions describe the same line
     */
    IDENTICAL,
    /**
     * The two functions are parallel
     */
    PARALLEL,
    /**
     * The two functions are skewed
     */
    SKEWED,
    /**
     * The two functions intersect at a specific point
     */
    INTERSECT
}
